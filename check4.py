# Ask for the user's age
age = float(input("Enter your age: "))

# Check if the age is between 10 and 20
if age >= 10:
    if age <= 20:
        print("Your age is between 10 and 20.")
    else:
        print("Your age is not between 10 and 20.")
else:
    print("Your age is not between 10 and 20.")
